#include<bits/stdc++.h>
using namespace std;
int main(){
    int x,y;
    cout <<"Enter your frist number  x = ";
    cin >> x;
    cout <<"Enter your second number y = ";
    cin >> y;


    cout <<"less than (<) operator .......................... "<<endl;
    cout <<" x < y :"<<( x < y ) <<endl;
    cout <<" y < x :"<<( y < x ) <<endl;

    cout <<"Greater than (>) operator ....................... "<<endl;
    cout <<" x > y :"<<( x > y ) <<endl;
    cout <<" y > x :"<<( y > x ) <<endl;

    cout <<"less than or Equal to (<=) operator ............. "<<endl;
    cout <<" x <= y :"<<( x <= y ) <<endl;
    cout <<" y <= x :"<<( y <= x ) <<endl;

    cout <<"Greater than or Equal to (>=) operator .......... "<<endl;
    cout <<" x >= y :"<<( x >= y ) <<endl;
    cout <<" y >= x :"<<( y >= x ) <<endl;

    cout <<"Equal to (==) operator .......................... "<<endl;
    cout <<" x == y :"<<( x == y ) <<endl;
    cout <<" y == x :"<<( y == x ) <<endl;

    cout <<"not Equal to (!=) operator ...................... "<<endl;
    cout <<" x != y :"<<( x != y ) <<endl;
    cout <<" y != x :"<<( y != x ) <<endl;






return 0;
}

